def scoreGreater(movieName: str, movieList: list, score: float = 5.5) -> bool:
  movie: dict = next((m for m in movieList if m["name"] == movieName), None)
  if(movie is None):
    raise Exception("Movie not found")
  return movie["imdb"] > score

def scoreAverage(movieList: list) -> float:
  return sum(movie['imdb'] for movie in movieList) / len(movieList)
